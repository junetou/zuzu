package org.andy.work.admin.detail;

public class TradeMessage {

	private String thingsid;
	private String lineid;
	private String name;
	private String thingsurl;
	private String infomation;
	private String price;
	private String addr;
	private String lineurl;
	private String picone;
	public String getThingsid() {
		return thingsid;
	}
	public void setThingsid(String id) {
		this.thingsid = id;
	}
	public String getLineid() {
		return lineid;
	}
	public void setLineid(String lineid) {
		this.lineid = lineid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getThingsurl() {
		return thingsurl;
	}
	public void setThingsurl(String thingsurl) {
		this.thingsurl = thingsurl;
	}
	public String getInfomation() {
		return infomation;
	}
	public void setInfomation(String infomation) {
		this.infomation = infomation;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getLineurl() {
		return lineurl;
	}
	public void setLineurl(String lineurl) {
		this.lineurl = lineurl;
	}
	public String getPicone() {
		return picone;
	}
	public void setPicone(String picone) {
		this.picone = picone;
	}
	
	
}
